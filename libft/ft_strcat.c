char	*ft_strcat(char *s1, const char *s2)
{
	int		i;
	char	*p;

	i = 0;
	p = s1;
	while (*p)
		++p;
	while (s2[i])
	{
		p[i] = s2[i];
		++i;
	}
	p[i] = '\0';
	return (s1);
}
