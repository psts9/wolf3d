#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	char	*p;

	i = 0;
	p = s1;
	while (*p)
		++p;
	while (i < n && s2[i])
	{
		p[i] = s2[i];
		++i;
	}
	p[i] = '\0';
	return (s1);
}
